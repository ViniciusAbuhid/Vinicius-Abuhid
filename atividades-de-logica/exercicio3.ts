function analyseStrings(str1:string, str2:string){
    const lowerStr1 = str1.toLowerCase()
    const lowerStr2 = str2.toLowerCase()
    const result = lowerStr1 === lowerStr2
    return result
  }
  
  function analyseStrings2(str1:string, str2:string){
    const result = str1 === str2
    return result
  }
  